    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($cat)
    {
       $cat=Cat::find($cat);
       return view('admin.category_edit',compact('cat'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {

        $rules = [
            'id'=>'required',
            'name' => 'required',
            'description' => 'required',
        ];

        // Perform validation
        $validator = Validator::make($request->all(), $rules);

        if ($validator->fails()) {
            return redirect()->back()->withErrors($validator)->withInput();
        }

        $validatedData = $validator->validated();

        $record = Cat::find($validatedData['id']);
        $record->name= $validatedData['name'];
        $record->description= $validatedData['description'];
        $record->save();
        return redirect("/admin/category2")->with('success', 'Cat inserted successfully!');

    }

